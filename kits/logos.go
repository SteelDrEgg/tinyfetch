func Logos(os string) [18]string {
	if os == "ubuntu" {
		return ubuntuLogo
	} else if os == "apple" {
		return appleLogo
	} else if os == "arch" {
		return archLogo
	} else if os == "linux" {
		return genericLogo
	}
	return [18]string{}
}
